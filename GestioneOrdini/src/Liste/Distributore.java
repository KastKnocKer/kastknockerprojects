package Liste;

public class Distributore {
	
	private String ID;
	private String Nome;
	
	public Distributore(){
		
	}

	public void setID(String iD) {
		ID = iD;
	}

	public String getID() {
		return ID;
	}

	public void setNome(String nome) {
		Nome = nome;
	}

	public String getNome() {
		return Nome;
	}

}
